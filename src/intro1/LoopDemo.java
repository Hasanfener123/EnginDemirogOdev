package intro1;

public class LoopDemo {

	public static void main(String[] args) {
		// For
		for(int i=1;i<=10;i++) {
			System.out.println(i);
			
		}
		System.out.println("Dongu bitti.");
		
		//While
		int i=3;
		while(i<=10) {
			System.out.println(i);
		i++;
		
		// Do-While Dongusu
		int j=1;
		do {
			System.out.println(j);
			j+=1;
		}
		while(j<10);
			
			
		}
		
	}

}
